void solve() {
    int n; cin>>n;
    vector<int> g[n + 1];
    for(int i = 0; i < n - 1; i++) {
        int u, v; cin>>u>>v;
        g[u].push_back(v);
        g[v].push_back(u);
    }

    function<int(int, int)> dfs = [&] (int u, int p) {
        int len = 0;
        for(auto v : g[u]) {
            if(v ^ p) {
                len ^= 1 + dfs(v, u);
            }
        }
        return len;
    };

    cout<<(dfs(1, 0) ? "Alice" : "Bob")<<endl;
}

