void solve() {
    int n; cin>>n;
    vector<int> v(n);
    for(auto &x : v) cin>>x;
    int p = v[0];
    for(int i = 1; i < n; i++) p &= v[i];

    auto binPow = [&] (int a, int b) {
        int ret = 1;
        while(b > 0) {
            if(b & 1) ret = (ret * a);
            a = (a * a);
            b >>= 1;
        }
        return ret;
    };
    
    int ans = 0;
    for(int i = 31; i >= 0; i--) {
        if(p >> i & 1) {
            ans += binPow(2, i);
        }
    }

    cout<<ans<<endl;
}

