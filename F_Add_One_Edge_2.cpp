void solve() {
    int n; cin>>n;
    vector<int> g[n + 1];
    for(int i = 0; i < n - 1; i++) {
        int u, v; cin>>u>>v;
        g[u].push_back(v);
        g[v].push_back(u);
    }

    vector<int> vis(n + 1);

    function<int(int, int)> dfs = [&] (int u, int p) {
        if(g[u].size() != 3) return (int) (g[u].size() == 2);
        vis[u] = 1;
        int ans = 0;
        for(auto v : g[u]) if(v ^ p) {
            ans += dfs(v, u);
        }
        return ans;
    };

    int ans = 0;

    for(int i = 1; i <= n; i++) {
        if(vis[i] || g[i].size() != 3) continue;
        int c = dfs(i, 0);
        ans += c * (c - 1) / 2;
    }

    cout<<ans<<endl;
}

