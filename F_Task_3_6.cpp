void solve() {
    set<int> st;
    int q; cin>>q;
    while(q--) {
        int t, x; cin>>t>>x;
        if(t == 1) {
            st.insert(x);
        } else if(t == 2) {
            if(st.count(x)) st.erase(x);
        } else {
            cout<<(st.count(x) ? "Yes" : "No")<<endl;
        }
    }
}

